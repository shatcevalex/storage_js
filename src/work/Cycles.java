package work;

public class Cycles {
    public static void main(String[] args) {
        // Программа которая выводит числа от 1 до 10 с помощью цикла for.
        for (int i = 0; i < 11; i++) {
            System.out.println(i);

        }
        // Dыводит числа от 10 до 1 с помощью цикла while.
        int j = 10;
        while (j  >= 0) {
            System.out.println(j);
            j--;
        }
    }
}
